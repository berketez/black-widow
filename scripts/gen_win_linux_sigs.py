def main():
    print("Generating Windows expanded signatures...")
    win_sigs = gen_windows_expanded()
    print(f"  Generated {len(win_sigs)} Windows signatures")

    print("Generating Linux expanded signatures...")
    linux_sigs = gen_linux_expanded()
    print(f"  Generated {len(linux_sigs)} Linux signatures")

    # Write Windows
    win_out = {
        "meta": {
            "generator": "karadul-sig-gen-win-linux",
            "date": "2026-04-05",
            "source": "windows_expanded",
            "count": len(win_sigs),
        },
        "signatures": win_sigs,
    }
    win_path = os.path.join(SIGS_DIR, "windows_expanded.json")
    with open(win_path, "w") as f:
        json.dump(win_out, f, separators=(",", ":"))
    fsize = os.path.getsize(win_path)
    print(f"  Wrote {win_path} ({fsize/1024:.0f} KB)")

    # Write Linux
    linux_out = {
        "meta": {
            "generator": "karadul-sig-gen-win-linux",
            "date": "2026-04-05",
            "source": "linux_expanded",
            "count": len(linux_sigs),
        },
        "signatures": linux_sigs,
    }
    linux_path = os.path.join(SIGS_DIR, "linux_expanded.json")
    with open(linux_path, "w") as f:
        json.dump(linux_out, f, separators=(",", ":"))
    fsize = os.path.getsize(linux_path)
    print(f"  Wrote {linux_path} ({fsize/1024:.0f} KB)")

    # Summary by category
    print("\n=== Windows Categories ===")
    cats = {}
    for v in win_sigs.values():
        c = v["category"]
        cats[c] = cats.get(c, 0) + 1
    for k, v in sorted(cats.items(), key=lambda x: -x[1]):
        print(f"  {k}: {v}")

    print("\n=== Linux Categories ===")
    cats = {}
    for v in linux_sigs.values():
        c = v["category"]
        cats[c] = cats.get(c, 0) + 1
    for k, v in sorted(cats.items(), key=lambda x: -x[1]):
        print(f"  {k}: {v}")

    # Check overlap with existing
    print("\n=== Overlap Check ===")
    try:
        with open(os.path.join(SIGS_DIR, "windows_api_signatures.json")) as f:
            existing_win = json.load(f)["signatures"]
        overlap_w = set(win_sigs.keys()) & set(existing_win.keys())
        new_w = set(win_sigs.keys()) - set(existing_win.keys())
        print(f"  Windows: {len(overlap_w)} overlap, {len(new_w)} genuinely new")
    except Exception as e:
        print(f"  Windows overlap check failed: {e}")

    try:
        with open(os.path.join(SIGS_DIR, "linux_signatures.json")) as f:
            existing_lin = json.load(f)["signatures"]
        overlap_l = set(linux_sigs.keys()) & set(existing_lin.keys())
        new_l = set(linux_sigs.keys()) - set(existing_lin.keys())
        print(f"  Linux: {len(overlap_l)} overlap, {len(new_l)} genuinely new")
    except Exception as e:
        print(f"  Linux overlap check failed: {e}")

    print("\nDone.")


if __name__ == "__main__":
    main()
